# Auth endpoints
@app.post("/register")
def register(user: UserCreate):
    if user.username in users:
        raise HTTPException(status_code=400, detail="Username already registered")
    global user_id_counter
    hashed_password = hash_password(user.password)
    new_user = User(id=user_id_counter, username=user.username, role=user.role)
    users[user.username] = {"id": user_id_counter, "username": user.username, "password": hashed_password, "role": user.role}
    user_id_counter += 1
    return {"message": "User registered successfully"}

@app.post("/login")
def login(user: UserLogin):
    db_user = users.get(user.username)
    if not db_user or not verify_password(user.password, db_user["password"]):
        raise HTTPException(status_code=401, detail="Invalid credentials")
    access_token_expires = timedelta(minutes=ACCESS_TOKEN_EXPIRE_MINUTES)
    access_token = create_access_token(
        data={"sub": user.username}, expires_delta=access_token_expires
    )
    return {"access_token": access_token, "token_type": "bearer"}

